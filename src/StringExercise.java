public class StringExercise {
    public static void main(String[] args) {
        String messageOne = "\nWe don't need no education\nWe don't need no thought control\n";
        System.out.println(messageOne);

        String messageTwo = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(messageTwo);

        String messageThree = "In windows, the main drive is usually C:\\\n";
        System.out.println(messageThree);

        String messageFour = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(messageFour);

    }
}